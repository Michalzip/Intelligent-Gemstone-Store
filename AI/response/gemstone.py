class Gemstone:
    def __init__(
        self,
        ID,
        Name,
        Image,
        CurrentPrice,
        DiscountPrice,
        OriginalPrice,
        FeedbackPercentage,
        FeedbackScore,
        ProfitabilityResult,
    ):
        self.ID = ID
        self.Name = Name
        self.Image = Image
        self.CurrentPrice = CurrentPrice
        self.DiscountPrice = DiscountPrice
        self.OriginalPrice = OriginalPrice
        self.FeedbackPercentage = FeedbackPercentage
        self.FeedbackScore = FeedbackScore
        self.ProfitabilityResult = ProfitabilityResult
